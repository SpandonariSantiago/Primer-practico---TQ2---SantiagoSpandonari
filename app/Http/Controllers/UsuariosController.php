    {
        $u = new Usuarios();
        $u -> NombreUsuario = $request -> input("NombreUsuario");
        $u -> NombreCompleto = $request -> input("NombreCompleto");
        $u -> Password = $request -> input("Password");
        if ($u -> save()){
            return view("AltaUsuario",["error" => False ]);    
        }else{
            return view("AltaUsuario",["error" => True ]); 
        };
    }

